package com.newroad.tripmaster.util;

public enum ProductFeatureDict {
  ZHE(1, "FA"), SU(2, "FB"), QIN(3, "FC"), QI(4, "FD"), CHI(5, "FE"), MI(6, "FF"), XUE(7, "FG"), YAN(8, "FH");

  private Integer code;
  private String tag;

  ProductFeatureDict(Integer code, String tag) {
    this.code = code;
    this.tag = tag;
  }

  public Integer getCode() {
    return code;
  }

  public void setCode(Integer code) {
    this.code = code;
  }

  public String getTag() {
    return tag;
  }

  public void setTag(String tag) {
    this.tag = tag;
  }
}
