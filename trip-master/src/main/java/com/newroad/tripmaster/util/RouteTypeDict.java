package com.newroad.tripmaster.util;

public enum RouteTypeDict {
  MOVE(1, "Move"), STAY(2, "Stay Hotel"), SCENIC(3, "Scenic"), RESTAURANT(4, "Restaurant"), PLAY(5, "Play"), SHOPPING(6, "Shopping"),
  AIR(7, "Air"),TRAIN(8,"Train"),CAR(9,"Car"),BUS(10,"Bus"),SUBWAY(11,"Subway"),WALK(12,"Walk"),SHIP(13,"Ship");

  private Integer code;

  private String mean;

  RouteTypeDict(Integer code, String mean) {
    this.code = code;
    this.mean = mean;
  }
  
  public Integer getCode() {
    return code;
  }

  public void setCode(Integer code) {
    this.code = code;
  }

  public String getMean() {
    return mean;
  }

  public void setMean(String mean) {
    this.mean = mean;
  }

}
