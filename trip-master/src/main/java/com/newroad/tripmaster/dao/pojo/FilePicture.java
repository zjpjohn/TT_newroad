package com.newroad.tripmaster.dao.pojo;

import java.io.Serializable;

import org.bson.types.ObjectId;
import org.mongodb.morphia.annotations.Entity;
import org.mongodb.morphia.annotations.Id;


@Entity(value="filepicture",noClassnameStored=true)
public class FilePicture implements Serializable{
  
  /**
   * 
   */
  private static final long serialVersionUID = -3640910517934948885L;

  @Id
  private ObjectId id;
  
  private String fileid;
  
  private String hashsiteid;
  
  private Integer type;
  
  private String url;
  
  private String userid;
  
  private String username;
  
  private String usericon;
  
  private Long createtime;
  
  private Long lastupdatetime;

  
  public ObjectId getId() {
    return id;
  }

  
  public void setId(ObjectId id) {
    this.id = id;
  }

  
  public String getFileid() {
    return fileid;
  }

  
  public void setFileid(String fileid) {
    this.fileid = fileid;
  }

  
  public String getHashsiteid() {
    return hashsiteid;
  }

  
  public void setHashsiteid(String hashsiteid) {
    this.hashsiteid = hashsiteid;
  }
  
  public Integer getType() {
    return type;
  }


  
  public void setType(Integer type) {
    this.type = type;
  }


  
  public String getUrl() {
    return url;
  }


  
  public void setUrl(String url) {
    this.url = url;
  }


  public String getUserid() {
    return userid;
  }

  
  public void setUserid(String userid) {
    this.userid = userid;
  }

  
  public String getUsername() {
    return username;
  }

  
  public void setUsername(String username) {
    this.username = username;
  }

  
  public String getUsericon() {
    return usericon;
  }

  
  public void setUsericon(String usericon) {
    this.usericon = usericon;
  }

  
  public Long getCreatetime() {
    return createtime;
  }

  
  public void setCreatetime(Long createtime) {
    this.createtime = createtime;
  }

  
  public Long getLastupdatetime() {
    return lastupdatetime;
  }

  
  public void setLastupdatetime(Long lastupdatetime) {
    this.lastupdatetime = lastupdatetime;
  }
  
  
}
