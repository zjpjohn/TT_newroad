package com.newroad.tripmaster.dao.pojo.info;

import java.io.Serializable;

import org.mongodb.morphia.annotations.Entity;
import org.mongodb.morphia.annotations.Id;


@Entity(value = "userbehavior", noClassnameStored = true)
public class UserBehavior implements Serializable{
  /**
   * 
   */
  private static final long serialVersionUID = 6591587233660621668L;
  @Id
  private String behaviorId;
  
  private String actionId;
  
  private Integer actiontype;
  
  private Integer actioncontent;
  
  private String targetId;
  
  private Long userId;
  
  private Integer status;
  
  private Long createTime;
  
  private Long updateTime;

  
  public String getBehaviorId() {
    return behaviorId;
  }

  public void setBehaviorId(String behaviorId) {
    this.behaviorId = behaviorId;
  }

  public String getActionId() {
    return actionId;
  }

  public void setActionId(String actionId) {
    this.actionId = actionId;
  }

  public Integer getActiontype() {
    return actiontype;
  }

  public void setActiontype(Integer actiontype) {
    this.actiontype = actiontype;
  }

  public Integer getActioncontent() {
    return actioncontent;
  }

  public void setActioncontent(Integer actioncontent) {
    this.actioncontent = actioncontent;
  }

  public String getTargetId() {
    return targetId;
  }

  public void setTargetId(String targetId) {
    this.targetId = targetId;
  }

  public Long getUserId() {
    return userId;
  }

  public void setUserId(Long userId) {
    this.userId = userId;
  }

  public Integer getStatus() {
    return status;
  }

  public void setStatus(Integer status) {
    this.status = status;
  }

  public Long getCreateTime() {
    return createTime;
  }

  public void setCreateTime(Long createTime) {
    this.createTime = createTime;
  }

  public Long getUpdateTime() {
    return updateTime;
  }

  public void setUpdateTime(Long updateTime) {
    this.updateTime = updateTime;
  }
  
  
}
