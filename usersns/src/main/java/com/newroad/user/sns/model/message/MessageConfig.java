package com.newroad.user.sns.model.message;

import java.io.Serializable;

/**
 * @info : 消息配置
 * @author: xiangping_yu
 * @data : 2013-11-12
 * @since : 1.5
 */
public class MessageConfig implements Serializable {

	private static final long serialVersionUID = 2252276915427938556L;
	/**
	 * 编号
	 */
	private int code;
	private String title;
	/**
	 * 消息内容格式
	 */
	private String msg;
	/**
	 * 类型范围
	 */
	private int scope;

	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public int getScope() {
		return scope;
	}
	public void setScope(int scope) {
		this.scope = scope;
	}
}
